package io.ridham._1_datatypes;

// These are numeric integral type datatypes
public class B {
	// 1-byte, default: 0
	byte b = 127;

	// 2-byte, default: 0
	short s = 32767;

	// 4-byte, default: 0
	int i = 2147483647;

	// 8-byte, default: 0L
	// Must use L to define explicitly as long value
	long l = 9223372036854775807L;
}
