package io.ridham._04_variables;

public class A {

	// Classification of variables can be done many ways
	// Based on datatypes: Premitive, Derived
	// Based on position: Instance, Static and Local
	public static void main(String[] args) {
		// Primitive datatypes
		// Total 8 types, cannot defined new primitive datatype
		int x = 10;
		
		// Derived datatypes
		// Some pre-defined by JAVA, and new datatype can be defined
		A a = new A();
	}

}
