package io.ridham._2_litarals;

// Any value assigned to datatype is literals
public class A {

	public static void main(String[] args) {
		// Decimal literal
		int x1 = 10;

		// Octal literal(add 0)
		int x2 = 010;

		// Hexadecimal literal(add 0X)
		int x3 = 0X0010;

		// Binary literal(add 0B)
		int x4 = 0B1101;

		System.out.println(x1+", "+x2+", "+x3+", "+x4);
	}
}
